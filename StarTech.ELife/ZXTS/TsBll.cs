        }
        /// <summary>
        /// 获得数据列表
        /// </summary>
        public List<TsModel> DataTableToList(DataTable dt)
        {
            List<TsModel> modelList = new List<TsModel>();
            int rowsCount = dt.Rows.Count;
            if (rowsCount > 0)
            {
                TsModel model;
                for (int n = 0; n < rowsCount; n++)
                {
                    model = dal.DataRowToModel(dt.Rows[n]);
                    if (model != null)
                    {
                        modelList.Add(model);
                    }
                }
            }
            return modelList;
        }

